function openBMIDialog() {
    const dialog = document.getElementById('BMIDialog');
    dialog.removeAttribute('hidden'); // Remove the hidden attribute
    dialog.showModal();
}

function closeBMIDialog() {
    const dialog = document.getElementById('BMIDialog');
    dialog.close();
    dialog.setAttribute('hidden', 'true'); // Re-add the hidden attribute
}


